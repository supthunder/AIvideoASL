#!/usr/bin/env node
// Scans public/signs/*.mp4 and writes lib/clips.generated.ts with the set of
// known glosses. Run before build/dev so the runtime lookup matches what's
// actually on disk. Wired into package.json as predev/prebuild.

import { readdir, writeFile, mkdir } from "node:fs/promises";
import { dirname, join, basename, extname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = dirname(__dirname);
const SIGNS_DIR = join(ROOT, "public", "signs");
const OUT_FILE = join(ROOT, "lib", "clips.generated.ts");

async function listMp4s(dir) {
  try {
    const entries = await readdir(dir, { withFileTypes: true });
    return entries
      .filter((e) => e.isFile() && extname(e.name).toLowerCase() === ".mp4")
      .map((e) => basename(e.name, extname(e.name)));
  } catch (err) {
    if (err.code === "ENOENT") return [];
    throw err;
  }
}

const signFiles = await listMp4s(SIGNS_DIR);
const letterFiles = await listMp4s(join(SIGNS_DIR, "letters"));

const glossList = signFiles
  .map((name) => name.toUpperCase())
  .sort();

const letterList = letterFiles
  .map((name) => name.toUpperCase())
  .filter((l) => l.length === 1)
  .sort();

const banner = `// AUTO-GENERATED by scripts/gen-clips-manifest.mjs. Do not edit by hand.
// Regenerated on every \`npm run dev\` / \`npm run build\` from public/signs/*.mp4.
`;

const body = `${banner}
export const KNOWN_GLOSSES: ReadonlySet<string> = new Set([
${glossList.map((g) => `  "${g}",`).join("\n")}
]);

export const FINGERSPELL_LETTERS: ReadonlySet<string> = new Set([
${letterList.map((l) => `  "${l}",`).join("\n")}
]);

export const CLIP_MANIFEST = {
  signs: ${glossList.length},
  letters: ${letterList.length},
  generatedAt: ${JSON.stringify(new Date().toISOString())},
} as const;
`;

await mkdir(dirname(OUT_FILE), { recursive: true });
await writeFile(OUT_FILE, body, "utf8");

console.log(
  `[gen-clips-manifest] wrote ${OUT_FILE} (${glossList.length} signs, ${letterList.length} letters)`,
);
